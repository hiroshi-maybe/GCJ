string S;

map<char,pair<ModInt,ModInt>> moves = { {'E',{0,1}}/*R*/,{'S',{1,0}}/*D*/,{'W',{0,-1}}/*L*/,{'N',{-1,0}}/*U*/ };

pair<ModInt,ModInt> f(int &p) {
  int N=SZ(S);
  pair<ModInt,ModInt> res={0,0};
  while(p<N&&(moves.count(S[p])||isdigit(S[p]))) {
    if(isdigit(S[p])) {
      auto rep=ModInt(S[p]-'0');
      ++p;
      assert(S[p]=='(');
      auto ans=f(++p);
      assert(S[p]==')');
      ++p;
      res.first+=ans.first*rep,res.second+=ans.second*rep;
    } else {
      char c=S[p];
      assert(moves.count(c));
      res.first+=moves[c].first,res.second+=moves[c].second;
      ++p;
    }
  }
  if(p<N) assert(S[p]==')');
  return res;
}
void solve() {
  int p=0;
  auto res=f(p);
  LL a=res.second.val,b=res.first.val;
  cout<<a+1<<" "<<b+1<<endl;
}

